//Arithmetic Operators

let a=10;
let b=5;

console.log(a+b);
console.log(a-b);
console.log(a/b);
console.log(a*b);

//Assingment Operators

console.log(a += 10);
console.log(a -= 10);
console.log(a /= 10);
console.log(a *= 10);
console.log(a %= 10);

//Comparison Operators

console.log(a == b);
console.log(a != b);
console.log(a > b);
console.log(a < b);
console.log(a <= b);
console.log(a === b);
console.log(a !== b);

//Logical Operators

console.log(a >= 5 && b <= 6);
console.log(a >= 5 && b <= 4);
console.log(a >= 5 || b <= 6);
console.log(a >= 5 || b <= 1);
console.log(!(a >= 6));

